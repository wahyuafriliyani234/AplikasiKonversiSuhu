    private void konversiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_konversiActionPerformed
         try {
        // Parsing input temperature
        double input = Double.parseDouble(txtInput.getText());

        // Get the selected units
        String fromUnit = getSelectedRadioButton(); // Unit of input
        String toUnit = jComboBox1.getSelectedItem().toString(); // Unit to convert to

        if (fromUnit.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Silakan pilih satuan input.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Perform conversion and show the result
        double result = convertTemperature(input, fromUnit, toUnit);
        txtHasil.setText(String.format("%.2f", result));

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Input tidak valid. Silakan masukkan angka.", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_konversiActionPerformed

