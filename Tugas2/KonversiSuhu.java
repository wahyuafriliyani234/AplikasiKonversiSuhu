private double convertTemperature(double input, String fromUnit, String toUnit) {
        switch (fromUnit) {
            case "fahrenheit":
                return fahrenheitToOther(input, toUnit);
            case "celcius":
                return celciusToOther(input, toUnit);
            case "reamur":
                return reamurToOther(input, toUnit);
            case "kelvin":
                return kelvinToOther(input, toUnit);
            default:
                return 0;
        }
    }

    // Implementasi konversi dari Fahrenheit ke satuan lainnya
    private double fahrenheitToOther(double input, String toUnit) {
        switch (toUnit) {
            case "celcius": return (input - 32) * 5 / 9;
            case "reamur": return (input - 32) * 4 / 9;
            case "kelvin": return (input - 32) * 5 / 9 + 273.15;
            case "fahrenheit": return input;
            default: return 0;
        }
    }

    // Implementasi konversi dari Celcius ke satuan lainnya
    private double celciusToOther(double input, String toUnit) {
        switch (toUnit) {
            case "fahrenheit": return (input * 9 / 5) + 32;
            case "reamur": return input * 4 / 5;
            case "kelvin": return input + 273.15;
            case "celcius": return input;
            default: return 0;
        }
    }

    // Implementasi konversi dari Reamur ke satuan lainnya
    private double reamurToOther(double input, String toUnit) {
        switch (toUnit) {
            case "fahrenheit": return (input * 9 / 4) + 32;
            case "celcius": return input * 5 / 4;
            case "kelvin": return (input * 5 / 4) + 273.15;
            case "reamur": return input;
            default: return 0;
        }
    }

    // Implementasi konversi dari Kelvin ke satuan lainnya
    private double kelvinToOther(double input, String toUnit) {
        switch (toUnit) {
            case "fahrenheit": return (input - 273.15) * 9 / 5 + 32;
            case "celcius": return input - 273.15;
            case "reamur": return (input - 273.15) * 4 / 5;
            case "kelvin": return input;
            default: return 0;
        }
    }


